void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin){
	if (GPIO_Pin == IN_Button_Pin){
		//Manual reset from external button
		MRT_resetFromStart();
	}
}


void HAL_RTC_AlarmAEventCallback(RTC_HandleTypeDef *hrtc){
	println("AlarmA");
	flagA = 1;
}

