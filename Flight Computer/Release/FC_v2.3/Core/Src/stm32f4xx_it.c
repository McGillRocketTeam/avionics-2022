void MRT_resetFromStart(void){

	//Clear memory
	MRT_clearExternalFlash();
	MRT_RTC_clearBackupRegs();

	//Shutdown Iridium
	//TODO should have deninit for every system?
	MRT_Iridium_Deinit();

	//Reset function
	NVIC_SystemReset();
}

